                <h2>Colaborá</h2>

                <h3>¿Por qué apoyar a Carpoolear?</h3>

                <p>Carpoolear es un proyecto colaborativo y sin fines de lucro de la asociación civil STS Rosario que permite que muchos viajeros se encuentren, coordinen y viajen juntos. Para los viajeros, tiene un beneficio social ya que se comparte un viaje, charlas, mates y establecen lazos sociales, y otro económico pues muchas veces los costos del viaje se reducen tanto para conductores como para pasajeros. </p>

                <p>Asimismo, con la práctica del carpooling se busca incentivar comportamientos sustentables en las personas a partir de la creación de una comunidad responsable y comprometida por llevar a cabo acciones cotidianas que ayuden a cuidar el medioambiente y así beneficiar a toda la sociedad.</p>

                <p>Pero Carpoolear no se mantiene andando solo y es llevado adelante por un grupo de voluntarios de la Asociación Civil STS Rosario. Es un proyecto colaborativo y sin fines de lucro… y queremos que siga así!!! Por eso necesitamos apoyo: podes ayudarnos con el desarrollo, difundir el proyecto, donarnos dinero, sumarte al equipo de voluntarios, cualquier tarea que creas que puede ayudarnos.</p>



