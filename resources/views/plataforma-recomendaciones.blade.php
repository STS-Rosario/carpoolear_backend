                <h2>Recomendaciones a la hora de Carpoolear</h2>

                <p>Compartir es una gran experiencia pero debemos tener en cuenta algunas consideraciones para poder carpoolear sin inconvenientes.</p>

                <h3>Origen y destino</h3>
                <p>Al crear un viaje que tenga origen y destino con direcciones precisas, nunca tenés que cargar direcciones de tu casa o similar. Siempre usar una Ciudad o un cruce de calles.</p>

                <h3>Comunicación entre usuarios</h3>
                <p>Según como hayas ingresado a la plataforma, recibirás notificaciones vía correo o notificaciones vía red social cuando hayan solicitudes, aceptaciones o rechazos para carpoolear, cuando se modifique o cancele un viaje, cuando te envíen mensajes, etc. </p>

                <h3>Carpooleando con desconocidos</h3>
                <p>Previo a carpoolear con usuarios de los que no tengas referencias (o sea, que no sean “amigos” o “amigos de tus amigos”), te recomendamos que te tomes unos minutos para revisar el perfil de Carpoolear de esa persona y su perfil en distintas redes sociales. De esa forma podrás contar con información acerca de la persona. También sugerimos conversar antes de viajar. Siempre es importante compartir con alguien que te genere confianza.</p>

                <h3>Acuerdos previos al viaje</h3>
                <p>Acordá previamente con el conductor el recorrido del viaje, si van a compartir los costos asociados al mismo, y cuánto deberá contribuir cada uno. Recordá que el conductor no puede lucrar con el viaje, ver <strong>“Seguro Automotor y la importancia de calcular el costo del viaje”</strong> (más abajo)</p>
                <p>Recomendamos que el pago se realice directamente al conductor al finalizar el viaje y sin intermediarios.</p>

                <h3>Antes de subirte a un auto o subir alguien a tu auto</h3>
                <ul class="lead" data-bullet="ti-control-play">
                  <li>Chequeá los DNI para verificar si la identidad de las personas se corresponde con la que te habían dicho.</li>
                  <li>En el momento del encuentro, chequeá que el modelo de auto y patente coincidan con el dato suministrado por el conductor.</li>
                  <li>Pedile al conductor que te muestre su carnet de conducir, la cédula verde del vehículo y el <strong>seguro del vehículo</strong> actualizado a la fecha. El auto también debe tener la inspección técnica obligatoria hecha.</li>
                  <li>Verificá visualmente que el auto en el que vas a viajar esté en buenas condiciones.</li>
                  <li>Verificá que haya un cinturón de seguridad para cada pasajero</li>
