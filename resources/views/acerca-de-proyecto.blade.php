                <h2>Acerca de Carpoolear</h2>
                
                <p>
                    <img class="flush-right" src="img/logo_sts_nuevo_color.png" width="170" height="50" alt="STS Rosario">
                    Carpoolear es un proyecto colaborativo sin fines de lucro de la asociación civil <a href="http://www.stsrosario.org.ar" target="_blank">STS Rosario</a>. Es un proyecto alineado con el objetivo de minimizar el impacto ambiental de nuestras actividades cotidianas. Se enmarca dentro de lo que se conoce como movilidad sostenible.</p>            
                  

                <h3>Misión</h3>

                <p>El objetivo de Carpoolear es masificar la práctica de carpooling e incentivar el uso racional y eficiente del automóvil como modo de transporte. Con ello se busca lograr disminuir:</p>

                

                <ul class="lead" data-bullet="ti-control-play">

                  <li>Las emisiones de gases causantes del aumento del efecto invernadero en nuestra atmósfera.</li>

                  <li>El consumo de combustibles fósiles (recursos no renovables).</li>

                  <li>Las congestiones de tránsito que ocasionan grandes pérdidas de tiempo.</li>

                  <li>Los niveles de smog y ruido en las urbes, causantes de enfermedades.</li>

                  <li>La probabilidad de accidentes de tránsito, con el consecuente descenso en la mortalidad.</li>

                </ul>

                <p>Para lograr esto, hemos desarrollado una plataforma gratuita, colaborativa y sin fines de lucro donde las personas pueden compartir con otras sus viajes en auto. </p>
                            
                <h3>¿Cómo impacta Carpoolear a nuestras vidas?</h3>
                
                <p>Cuando una persona carpoolea ocurren varios hechos en simultáneo que afectan la vida de esa persona y la de otros. </p>
                
                <p>En primer lugar, uno de los mayores impactos positivos de Carpoolear es reforzar los lazos sociales. Al compartir un viaje, personas que muchas veces no se conocían entre sí, generan un vínculo a través de las charlas del viaje. Surgiendo posibilidades de trabajo y nuevos contactos, intercambiando puntos de vista sobre distintos temas, generando confianza entre la gente, formando hasta un sentimiento de “comunidad carpoolera”. Esto va logrando un cambio cultural frente a las personas desconocidas, además de viajes mucho más entretenidos y divertidos.</p>
                <p>En segundo lugar, la práctica del carpooling genera un impacto ambiental positivo cuando dos conductores de automóviles que habitualmente viajan en autos separados deciden unirse y viajar en un mismo vehículo dejando el otro guardado en el garage. Si esto se hace habitual en las personas podríamos reducir la cantidad de autos circulantes y así disminuir también el tiempo de viaje, las emisiones de gases de efecto invernadero, el consumo de combustibles fósiles (recursos no renovables), el smog y la contaminación sonora que provocan los vehículos, entre otros beneficios. </p>
                <p>La mayoría de los viajes que se cargan actualmente en la plataforma no generan un impacto ambiental directo. Los mismos son viajes interurbanos que se realizan ocasionalmente los fines de semana. Sin embargo, sirven para que las personas conozcan la práctica, se animen a viajar con desconocidos y encuentren una nueva forma de viaje. En un futuro, cuando se logre que la práctica se vuelva masiva, esperamos que las personas consideren el compartir el auto como algo viable para aplicar tanto en sus viajes ocasionales de fin de semana como a los de rutina, ir al trabajo, escuela, etc. También esperamos que más conductores se dejen llevar en auto ajeno y dejen el suyo guardado. </p>
                <p>En tercer lugar, compartir el auto implica un beneficio económico para los viajeros. Cuando se finaliza un viaje se calcula el costo de combustible y peajes y se divide por la cantidad de ocupantes del vehículo. Entonces, cuando aumenta el número de ocupantes, se disminuye el costo del viaje y el mismo se vuelve más accesible. </p>
                <p>El carpooling tiene diferentes impactos positivos sobre la sociedad, alienta la sociabilización entre desconocidos, disminuye la cantidad de autos circulantes con los beneficios que esto significa y representa un beneficio económico para los usuarios. Por eso creemos importante alentar esta práctica y concientizar a las personas para que hagan un uso más racional del automóvil.</p>
                
                <h3>Impacto Ambiental</h3>
                <p>Cada vez que dos automovilistas deciden compartir el viaje en un solo vehículo, se evitan – en promedio, por kilómetro recorrido – la emisión de 150 gr de CO2 a la atmósfera, uno de los principales gases que contribuyen al efecto invernadero, y el consumo de 0,071 litros de nafta, fuente de energía no renovable y que por ello debe utilizarse de forma racional.</p>

